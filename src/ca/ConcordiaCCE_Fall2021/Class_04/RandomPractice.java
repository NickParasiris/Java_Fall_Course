package ca.ConcordiaCCE_Fall2021.Class_04;

public class RandomPractice {

    public static void main(String[] args) {

        //Random value between 1 and 10

        double randomValue1 = Math.random() * 10;
        System.out.println(randomValue1);

        //Random value between 1 and 100
        double randomValue2 = Math.random() * 100;
        System.out.println(randomValue2);

        //Random value between 50 and 100
        double randomValue3 = 50 + Math.random() * 50;
        System.out.println(randomValue3);



    }
}
