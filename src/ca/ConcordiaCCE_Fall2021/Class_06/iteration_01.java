package ca.ConcordiaCCE_Fall2021.Class_06;

public class iteration_01 {
    public static void main(String[] args) {

        //While

        int counter = 0;

        while (counter <= 10)
        {
            System.out.println(counter);
            counter++;
        }

        System.out.println();
        //Do while

        int counter2 = 15;

        do
        {
            System.out.println(counter2);
            counter2++;
        } while (counter2 < 10);











    }
}
